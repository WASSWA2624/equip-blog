function prefixSeries(prefix, values) {
  return values.map((value) => `${prefix} ${value}`.trim());
}

function suffixSeries(values, suffix) {
  return values.map((value) => `${value} ${suffix}`.trim());
}

function normalizeCatalogName(name) {
  return `${name || ""}`
    .trim()
    .replace(/\s+/g, " ")
    .replace(/\b\w/g, (character) => character.toUpperCase());
}

